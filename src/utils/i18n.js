export function useT() {
  const language = useStore(s => s.language)
  const lang = DICT[language] ? language : 'en'
  return (key) => DICT[lang][key] ?? DICT.en[key] ?? key
}

// Helpers for dynamic strings
export function useFmt() {
  const language = useStore(s => s.language)
  const lang = DICT[language] ? language : 'en'
  const d = DICT[lang]
  return {
    ago: (mins) => {
      if (mins < 60) return `${mins}${d.time_m} ${d.time_ago}`
      if (mins < 1440) return `${Math.round(mins / 60)}${d.time_h} ${d.time_ago}`
      return `${Math.round(mins / 1440)}${d.time_d} ${d.time_ago}`
    },
    daysAgo: (days) => days === 0 ? d.time_today : `${days}${d.time_d} ${d.time_ago}`,
    timeframe: (str) => {
      if (!str) return str
      const s = String(str)
      const num = s.match(/\d+/)?.[0] || ''
      if (/hour/i.test(s)) return `${num} ${d.tf_hours}`
      if (/day/i.test(s)) return `${num} ${d.tf_days}`
      if (/all/i.test(s)) return d.tf_all
      return s
    },
  }
}
